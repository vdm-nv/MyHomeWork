from pprint import pprint

topology_example = {('R1', 'Eth0/0'): ('SW1', 'Eth0/1'),
                    ('R2', 'Eth0/0'): ('SW1', 'Eth0/2'),
                    ('R2', 'Eth0/1'): ('SW2', 'Eth0/11'),
                    ('R3', 'Eth0/0'): ('SW1', 'Eth0/3'),
                    ('R3', 'Eth0/1'): ('R4', 'Eth0/0'),
                    ('R3', 'Eth0/2'): ('R5', 'Eth0/0'),
                    ('SW1', 'Eth0/1'): ('R1', 'Eth0/0'),
                    ('SW1', 'Eth0/2'): ('R2', 'Eth0/0'),
                    ('SW1', 'Eth0/3'): ('R3', 'Eth0/0')}
class Topology:
    def __init__(self, top):
        self.topology = self._normalize(top)

    def _normalize(self, top):
         self.res = {}
         for key,value in top.items():
             if value not in self.res.keys():
                 self.res[key] = value
         return self.res

    def delete_link(self, port01, port02):
        flag = True
        for k,v in list(self.res.items()):
            if port01 == k and port02 == v or port02 == k and port01 == v:
                self.res.pop(k)
                flag = False
        if flag:
            print('Такого соединения нет')

if __name__ == "__main__":
    top = Topology(topology_example)
    pprint(top.topology)
    top.delete_link(('R3', 'Eth0/2'), ('R5', 'Eth0/0'))
    print("Removing ('R3', 'Eth0/2'), ('R5', 'Eth0/0')")
    pprint(top.topology)
    print("Try to remove again ('R3', 'Eth0/2'), ('R5', 'Eth0/0')")
    top.delete_link(('R3', 'Eth0/2'), ('R5', 'Eth0/0')) 
